user_interrupt:
	.frame	$fp,8,$31		# vars= 0, regs= 1/0, args= 0, gp= 0
	.mask	0x40000000,-4
	.fmask	0x00000000,0
	.set	noreorder
	.set	nomacro
	addiu	$sp,$sp,-8
	sw	$fp,4($sp)
	move	$fp,$sp
	nop
	move	$sp,$fp
	lw	$fp,4($sp)
	addiu	$sp,$sp,8
	jr	$31
	nop

	.set	macro
	.set	reorder
	.end	user_interrupt
	.size	user_interrupt, .-user_interrupt
	.align	2
	.globl	main
	.set	nomips16
	.set	nomicromips
	.ent	main
	.type	main, @function
main:
	.frame	$fp,32,$31		# vars= 8, regs= 2/0, args= 16, gp= 0
	.mask	0xc0000000,-4
	.fmask	0x00000000,0
	.set	noreorder
	.set	nomacro
	addiu	$sp,$sp,-32
	sw	$31,28($sp)
	sw	$fp,24($sp)
	move	$fp,$sp
	move	$4,$0
	jal	spi_mode_set
	nop

$L3:
	jal	spi_slave_read
	nop

	sw	$2,16($fp)
	lw	$2,16($fp)
	nop
	andi	$2,$2,0x00ff
	move	$4,$2
	jal	putch
	nop

	li	$4,10			# 0xa
	jal	putch
	nop

	lw	$2,16($fp)
	nop
	move	$4,$2
	jal	xtoa
	nop

	move	$4,$2
	jal	puts
	nop

	li	$4,10			# 0xa
	jal	putch
	nop

	b	$L3
	nop

	.set	macro
	.set	reorder
	.end	main
	.size	main, .-main
